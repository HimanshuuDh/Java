public class ComputeArea {
    public static void main(String[] args){
        double radius; //Declares radius
        double area;   // " area

        //Assign a radius
        radius = 20;

        //Compute area
        area = radius * radius * 3.14159;

        //Display results
        System.out.println("The area of circle of radius " + radius + " is " + area);
    }
}
